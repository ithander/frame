package org.ithang.auth.bean;

/**
 * 角色表
 * @author ithang
 *
 */
public class Role {

	private String id;
	private String title;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
}
